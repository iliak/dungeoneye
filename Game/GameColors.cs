			}
		}

		/// <summary>
		/// 
		/// </summary>
		static public Color Yellow
		{
			get
			{
				return Color.FromArgb(255, 255, 255, 85);
			}
		}

		/// <summary>
		/// 
		/// </summary>
		static public Color LightBlue
		{
