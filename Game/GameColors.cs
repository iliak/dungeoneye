	{

		/// <summary>
		/// 
		/// </summary>
		static public Color White
		{
			get
			{
				return Color.White;
			}
		}

		/// <summary>
		/// 
		/// </summary>
		static public Color Black
		{
			get
			{
				return Color.Black;
			}
		}

		/// <summary>
		/// 
		/// </summary>
		static public Color LightGrey
		{
