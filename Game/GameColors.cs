				return Color.FromArgb(255, 44, 48, 138);
			}
		}

		/// <summary>
		/// 
		/// </summary>
		static public Color Green
		{
			get
			{
				return Color.FromArgb(255, 0, 170, 0);
			}
		}

		/// <summary>
		/// 
		/// </summary>
		static public Color Main
		{
